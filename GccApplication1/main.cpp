int initialize_micro(void){
	// start ADC for lm335
	ADMUX = (1<<REFS0);
	ADCSRA = (1<<ADEN) |(1<<ADPS2)|(1<<ADPS1)|(1<<ADPS0);
	
	// initialize btns
	DDRD &=~( (1 << btn_left)  | (1 << btn_right) );
	PORTD |= (1 << btn_left)  |(1 << btn_right);
	
	DDRB &=~(1<<btn_currnt_mode);
	PORTB |=(1<<btn_currnt_mode);
	
	EICRA |= (1<<ISC01) | (1<<ISC11);
	EICRA &= ~((1<<ISC00) | (1 <<ISC10));
	EIMSK = (1 <<INT0) | (1 <<INT1);
	sei();
}

ISR(INT0_vect) {
	_delay_ms(30);
	if ((PIND & (1 << btn_left)) == 0)
		if(fan_speed !=0)
			fan_speed-=step_fan_speed;
}

ISR(INT1_vect) {
	_delay_ms(30);
	if ((PIND & (1 << btn_right)) == 0)
		if(fan_speed!=100)
			fan_speed+=step_fan_speed;
}

uint16_t ADC_Read(uint8_t channel){
	channel &= 0X07;
	ADMUX = (ADMUX &0XF8) | channel;
	
	ADCSRA |= (1<<ADSC) ;
	while(ADCSRA &(1<<ADSC));
	
	return ADC;
}

uint16_t ADC_Read_Average(uint8_t channel ,uint8_t num_repeat=10){
	uint16_t sum = 0;
	for(int i =0;i<=num_repeat;i++){
		sum+=ADC_Read(channel);
		_delay_ms(5);
	}
	return sum /num_repeat;
}

